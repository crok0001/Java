
public class Practice01StringPrintExample {
	
	// 프로그램 실행 시에 호출
	// 호출된 기능이 완료되면 프로그램도 종료
	public static void main(String[] args) {
		System.out.println("Main 시작");
		
		// 문자열을 저장하는 데이터 타입 String
		// String type의 변수 str을 선언하고 "Hello Java"라는 문자열로 
		// 초기화
		String str = "Hello Java";
		System.out.println(str);
		
		System.out.println("Main 종료");
	}

}





